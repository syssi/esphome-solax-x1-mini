#pragma once
#include <cstdint>
#include <vector>

namespace esphome::solax_meter_gateway::testing {

// Source: solax_meter_modbus.cpp comments (real inverter traffic)
// SolaxMeterModbus strips the address byte (data_offset=1) and passes
// the remaining 5 bytes (data_len=5) to on_solax_meter_modbus_data().
// data[2] is the register address that drives the switch() in the gateway.

// Raw: 0x01 0x03 0x00 0x0B 0x00 0x01 0xF5 0xC8  (Handshake)
static const std::vector<uint8_t> HANDSHAKE_REQUEST = {0x03, 0x00, 0x0B, 0x00, 0x01};

// Raw: 0x01 0x04 0x00 0x0C 0x00 0x02 0xB1 0xC8  (Read power 32-bit float)
static const std::vector<uint8_t> READ_POWER_32BIT_FLOAT_REQUEST = {0x04, 0x00, 0x0C, 0x00, 0x02};

// Raw: 0x01 0x03 0x00 0x0E 0x00 0x01 0xE5 0xC9  (Read power 16-bit sint)
static const std::vector<uint8_t> READ_POWER_16BIT_SINT_REQUEST = {0x03, 0x00, 0x0E, 0x00, 0x01};

// Raw: 0x01 0x04 0x00 0x48 0x00 0x02 0xF1 0xDD  (Read total energy import)
static const std::vector<uint8_t> READ_TOTAL_ENERGY_IMPORT_REQUEST = {0x04, 0x00, 0x48, 0x00, 0x02};

// Raw: 0x01 0x04 0x00 0x4A 0x00 0x02 0x50 0x1D  (Read total energy export)
static const std::vector<uint8_t> READ_TOTAL_ENERGY_EXPORT_REQUEST = {0x04, 0x00, 0x4A, 0x00, 0x02};

// Raw: 0x01 0x03 0x00 0x08 0x00 0x04 0xC5 0xCB  (Read total energy)
static const std::vector<uint8_t> READ_TOTAL_ENERGY_REQUEST = {0x03, 0x00, 0x08, 0x00, 0x04};

}  // namespace esphome::solax_meter_gateway::testing
